    //sets the array list as all mentors
    public void setArray(){
        list = database.getMentorsList();
    }
    
    //converts array list to array
    public void convertArray(){
        info = new String[list.size()];
        for (int i = 0; i < list.size(); i++) {
            info[i] = list.get(i);
        }
    }
    
    //displays array in JList
    public void populateMList(){
        setArray();
        convertArray();
        DefaultListModel dlm = new DefaultListModel();
        for (int i = 0; i < info.length; i++) {
            dlm.addElement(info[i]);
        }
        mentorList.setModel(dlm);
    }
    
    
