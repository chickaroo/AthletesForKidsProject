    //when a mentor is double clicked on, show the events of that mentor
    private void mentorListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mentorListMouseClicked
        JList theList = (JList) evt.getSource();
        if (evt.getClickCount() == 2){
            int index = theList.locationToIndex(evt.getPoint());
            if (index >= 0){
                Object o = theList.getModel().getElementAt(index);
                tempMentor = o.toString();
                //System.out.println("Double-clicked on: " + tempMentor);
                //System.out.println(o.toString());
                setSelectedID(tempMentor);
                //frameInstance.p3.test();
                frameInstance.p3.populateEList(this.id);
                ((AppDraftGUI) this.getRootPane().getParent()).showPanel(3);
            }
        }
    }//GEN-LAST:event_mentorListMouseClicked

